package com.qili.crawlertest;

public class Member {
	private String name;
	private String job;
	private String major;
	private String degree;
	private String acaYears;
	private boolean inCharge;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getJob() {
		return job;
	}
	public void setJob(String job) {
		this.job = job;
	}
	public String getMajor() {
		return major;
	}
	public void setMajor(String major) {
		this.major = major;
	}
	public String getDegree() {
		return degree;
	}
	public void setDegree(String degree) {
		this.degree = degree;
	}
	public String getAcaYears() {
		return acaYears;
	}
	public void setAcaYears(String acaYears) {
		this.acaYears = acaYears;
	}
	public boolean isInCharge() {
		return inCharge;
	}
	public void setInCharge(boolean inCharge) {
		this.inCharge = inCharge;
	}
}
